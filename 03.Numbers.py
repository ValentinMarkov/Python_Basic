from math import *

# print numbers and actions
print(22)
print(-122.345)
print(22 - 12)
print((22 - 12) + 145)

# divide and reminder
print(11 % 3)

# variables and numbers
num_a = 40
num_b = 20
print(num_a + num_b)
print(num_a + 12.45)

num_c = num_a + 100
print(num_c)

# convert number to string
num_x = 300
print(str(num_x) + " is good number")

# get absolut value
num_z = -25
print(abs(num_z))

a = -19
print(abs(a))

# returns the power of a number
print(pow(2, 3))
print(pow(4, 2))

# max number
print(max(12, 1, 22, 9))

# min number
print(min(12, 1, -22, 9))

# round number
print(round(2.3))
print(round(3.5))
print(round(3.6))

# chop off decimal/round down
print(floor(4.56))

# round up
print(ceil(4.56))

# square root of any number
print(sqrt(9))
print(sqrt(25))
